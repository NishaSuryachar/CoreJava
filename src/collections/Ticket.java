package collections;

public class Ticket
{
   int ticket_no;
   String passenger_name;
   String departure;
   String destination;
   Ticket(int ticket_no,String passenger_name,String departure,String destination)
   {
	   this.ticket_no=ticket_no;
	   this.passenger_name=passenger_name;
	   this.departure=departure;
	   this.destination=destination;
   }
}
